let currentLang = localStorage.getItem('nvl-lang') || 'vi';

function t(key, vars) {
  let s = (translations[currentLang] || translations.vi)[key] || key;
  if (vars) for (const k of Object.keys(vars)) s = s.replace(new RegExp('{'+k+'}','g'), vars[k]);
  return s;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('nvl-lang', lang);
  document.getElementById('html-root').lang = lang;
  ['vi','en','th'].forEach(l => document.getElementById('lang-'+l).classList.toggle('active', l === lang));
  document.querySelectorAll('[data-i18n]').forEach(el => { el.textContent = t(el.dataset.i18n); });
  const hint = document.getElementById('import-hint');
  if (hint) {
    const allReady = state.filesReady.inv && state.filesReady.aging && state.filesReady.price;
    hint.textContent = allReady ? t('importReady') : t('importHint');
  }
  buildFilterSelects();
  if (!document.getElementById('phase-results').classList.contains('hidden')) {
    renderSummary(); renderAlerts(); renderTable();
    if (!document.getElementById('view-chart').classList.contains('hidden')) renderChart();
  }
}
